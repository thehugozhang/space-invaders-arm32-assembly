UpdateNextLaser:
	// Update positions of bitmaps.
	// r0 - pixmap control ptr.
	// r1 - pixmap ptr.
	// r2 - spaceship control ptr.
	// r3 - laser statuses ptr.
	// Prologue.
	push {r4, r5, r6, r7, r8, r9, r10, r11, lr}
	
	// Store lasers bitmap.
	mov r4, r1
	
	// Get spaceship x-pos.
	ldrb r2, [r2, #PIXMAP_XPOS]
	
	// TODO Need to adjust positions 265-310 here.
    cmp r2, #0x9
    beq correctLaserByte
    cmp r2, #0x18
    beq correctLaserByte
    cmp r2, #0x27
    beq correctLaserByte
    cmp r2, #0x36
    beq correctLaserByte

    b correctLaserDraw

    correctLaserByte:
        ldr r9, =#0x10f
        add r2, r9, r2

    correctLaserDraw:	
        // Get next laser count.
        ldrb r9, [r0, #LASER_COUNT] // this is using pixmap_xpos bc I didn't align count yet.
        ldrb r7, [r0, #41]
	
	// Set next laser xpos to center of spaceship and y to just above spaceship.
	// Also set yvel to -8.
	mov r1, r2 // xpos
	mov r6, #0 // xvel
	mov r2, #212 //ypos
	mov r8, #8 //yvel
	mov r11, #1 //status
	
	// Update control values of current laser.
	add r10, r9, #PIXMAP_XPOS
	strb r1, [r0, r10]
	
	add r10, r9, #PIXMAP_XVEL
	strb r6, [r0, r10]
	
	add r10, r9, #PIXMAP_YPOS
	strb r2, [r0, r10]

	add r10, r9, #PIXMAP_YVEL
	strb r8, [r0, r10]
	
	strb r11, [r3, r7]
		
	// Update to next laser count.
	add r10, r9, #4
	add r7, r7, #1
	
