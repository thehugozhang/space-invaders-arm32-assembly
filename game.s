UpdatePos:
	// Update positions of bitmaps.
	// r0 - pixmap control ptr.
	// r1 - pixmap ptr.
	// Prologue.
	push {r4, r5, r6, r7, r8, r9, lr}
	
	// Store bitmap.
	mov r4, r1
	
	// Retrieve control values.
	ldrb r5, [r0, #PIXMAP_XPOS]
	ldrb r6, [r0, #PIXMAP_XVEL]
	ldrb r7, [r0, #PIXMAP_YPOS]
	ldrb r8, [r0, #PIXMAP_YVEL]
	
	// Loading bytes meaning its get cut off at xpos 265 because its 0x109 and loads 0x09 only.
	cmp r5, #0x9
	beq correctByte
	cmp r5, #0x18
	beq correctByte
	cmp r5, #0x27
	beq correctByte
	cmp r5, #0x36
	beq rightBoundary
	
	// Update coords.
	// Check if need to reverse direction.
	cmp r6, #0xf1
	beq subPos
	add r1, r5, r6
	b continueUpdate
	
	subPos:
		cmp r5, #0xa
		mov r1, r5
		beq continueUpdate
		sub r1, r5, #15
		b continueUpdate
	
	correctByte:
		mov r9, r5
		ldr r1, =#0x10f
		add r1, r5, r1
		cmp r6, #0xf1
		beq correctByteNeg
		b continueUpdate
	correctByteNeg:
		sub r1, r1, #30
		b continueUpdate
	rightBoundary:
		add r1, r5, #0x100
		cmp r6, #0xf1
		beq rightBoundaryNeg
		b continueUpdate
		
	rightBoundaryNeg:
		sub r1, r1, #15

	continueUpdate:
		add r2, r7, r8
	
	// Store updated control values.
	strb r1, [r0, #PIXMAP_XPOS]
	strb r2, [r0, #PIXMAP_YPOS]
	strb r6, [r0, #PIXMAP_XVEL]
	strb r8, [r0, #PIXMAP_YVEL]
		
	// Update bitmap pos using velocity.
	mov r0, r4

	// Draw bitmap.
	bl BitBlit
	
	// Epilogue.
	pop {r4, r5, r6, r7, r8, r9, pc}

UpdateLasers:
	// Update positions of laser bitmaps.
	// r0 - pixmap control ptr.
	// r1 - pixmap ptr.
	// r2 - laser statuses.
	// Prologue.
	push {r4, r5, r6, r7, r8, r9, r10, r11, lr}
	
	mov r10, #0
	b updateShotLaserCondition
	
	updateShotLaserBody:
		mov r4, #4
		mul r11, r10, r4
		
		
		add r4, r0, r11

		// Compare laser status value
		ldrb r7, [r2, #0] // laser status value
		cmp r7, #0
		beq skipUpdateLasers

		// Retrieve control values.
		ldrb r8, [r4, #PIXMAP_XPOS]
		ldrb r5, [r4, #PIXMAP_YPOS]
		ldrb r6, [r4, #PIXMAP_YVEL]

		// adjust x for end screen

		cmp r8, #0x9
		beq correctShotLaserByte
		cmp r8, #0x18
		beq correctShotLaserByte
		cmp r8, #0x27
		beq correctShotLaserByte
		cmp r8, #0x36
		beq correctShotLaserByte
		cmp r8, #0x45
		beq correctShotLaserByte

		b correctShotLaserDraw

		correctShotLaserByte:
			ldr r9, =#0xf0
			add r8, r9, r8

		correctShotLaserDraw:
			sub r5, r5, r6

		strb r5, [r4, #PIXMAP_YPOS]

		mov r0, r1
		mov r1, r8
		mov r2, r5
		// Draw bitmap.
		bl BitBlit
		
		ldr r0, =laser_controls
		ldr r1, =laser
		ldr r2, =laser_statuses
	
	skipUpdateLasers:
		add r10, r10, #1
	updateShotLaserCondition:
		cmp r10, #20
		blt updateShotLaserBody
	
	// Epilogue.
	pop {r4, r5, r6, r7, r8, r9, r10, r11, pc}

