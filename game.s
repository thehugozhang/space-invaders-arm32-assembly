DrawStr:
	// Draw a string at coords (x,y).
	// r0 - x-coord.
	// r1 - y-coord.
	// r2 - string.
	// Prologue.
	push {r4, r5, r6, lr}
	
	mov r4, r0
	mov r5, r1
	mov r6, r2
	
	// Load first character.
	ldrb r3, [r6]
	
	do:
		bl DrawChar
	while:
		// Increase x coord and memory address.
		add r4, r4, #1
		mov r0, r4
		add r6, r6, #1
		ldrb r3, [r6]
		
		// If reach end of string, exit. Else continue.
		cmp r3, #0
		bne continue
		b exit
	// If reach end of display, exit. Else, do.
	continue:
		cmp r0, #79
		blt do
	exit:
		// Epilogue.
		pop {r4, r5, r6, pc}


DrawChar:
	// Helper function that draws ASCII character at coords (x,y).
	// r0 - x-coord.
	// r1 - y-coord.
	// r2 - ascii code.
	// Prologue.
	push {r4, r5, r6, r7, r8, r9, lr}

	mov r4, r0 // r4 = x
	mov r5, r1 // r5 = y
	mov r6, r3

	// Convert coords to corresponding address.
	ldr r7, =TEXT_BUFFER
	lsl r8, r5, #7
	add r9, r8, r4

	// Draw.
	strb r6, [r7, r9]
	
	// Epilogue.
	pop {r4, r5, r6, r7, r8, r9, pc}
