BitBlit:
	// Draws a pixelmap from a pointer at coords (x,y). 
	// r0 - pixmap ptr.
	// r1 - center x-coord.
	// r2 - center y-coord.
	// Prologue.
	push {r4, r5, r6, r7, r8, r9, r10, r11, lr}

	mov r4, r0
	mov r5, r1
	mov r6, r2

	// Counters.
	mov r3, #0
	mov r11, #0

	// Load starting addresses values in registers.
	ldrh r7, [r4, #PIXMAP_WIDTH]
	ldrh r8, [r4, #PIXMAP_HEIGHT]
	ldrh r9, [r4, #PIXMAP_TRANSPARENCY]
	ldrh r10, [r4, #PIXMAP_PIXELDATA]
	
	// Calculate center offset.
	mov r0, r7
	bl RemainderDiv
	sub r5, r0
	
	mov r0, r8
	bl RemainderDiv
	sub r6, r0
	
	b bb_width_for_loop

	increment_bb_width:
		// Load next pixel address into r10.		
		mul r10, r7, r11		
		add r10, r10, r3
		
		mov r0, r10
		bl MulByTwo
		mov r10, r0
		
		add r10, r10, r4
		ldrh r10, [r10, #PIXMAP_PIXELDATA]

		// Add count to top left.
		add r0, r5, r3
		add r1, r6, r11
		mov r2, r10
		
		cmp r9, r10
		beq bb_skip_transparency
		
		bl DrawPixelBuffer
	
	bb_skip_transparency:
		add r3, r3, #1
	bb_width_for_loop:
		// If reach end of width, continue to next line.
		cmp r3, r7
		bne increment_bb_width
		
		add r11, #1

		// If reach end of height, finish.
		cmp r11, r8
		beq bb_finish_clear

		// Reset counters.
		mov r3, #0
		
		b increment_bb_width


	bb_finish_clear:
		// Epilogue.
		pop {r4, r5, r6, r7, r8, r9, r10, r11, pc}

MulByTwo:
	// Prologue.
	push {r4, r5, lr}
	
	mov r4, r0
	mov r5, #2
	mul r0, r4, r5
	
	// Epilogue.
	pop {r4, r5, pc}

RemainderDiv:
	// Perform repeated division by 2 and return quotient.
	// r0 - dividend.
	// Prologue.
	push {r4, r5, lr}

	mov r4, r0
	mov r5, #0 // Quotient.
	
	// While loop from hint.
	b condition_test
	body:
		sub r4, r4, #2
		add r5, r5, #1
	condition_test:
		cmp r4, #2
		bge body
	// Return quotient.
	mov r0, r5
	
	// Epilogue.
	pop {r4, r5, lr}
	bx lr
	
